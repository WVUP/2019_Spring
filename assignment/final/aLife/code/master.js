function fetchPkmn() {
    fetch(baseUrl + 'pokedex/1/')
        .then(res => res.json())
        .then(data => {
            for (let i = 0; i < data.pokemon_entries.length; i++) {
                pkmnList.push({
                    ID: data.pokemon_entries[i].entry_number,
                    NAME: data.pokemon_entries[i].pokemon_species.name,
                    PKMNURL: data.pokemon_entries[i].pokemon_species.url
                });
            }
            pkmnListDiv.innerHTML = "";
            for (let i = 0; i < pkmnList.length; i++) {
                pkmnListDiv.innerHTML += `<p id="${pkmnList[i].NAME}">${pkmnList[i].NAME}</p>`;
            }
            //console.log(pkmnList);
        });
}

function fetchTypes() {
    fetch(baseUrl + 'type/')
        .then(res => res.json())
        .then(data => {
            for (let i = 0; i < data.results.length; i++) {
                pkmnTypes.push({
                    TYPE: data.results[i].name,
                    TYPEURL: data.results[i].url
                });
            }
            //console.log(pkmnTypes);
            for (let i = 0; i < pkmnTypes.length; i++) {
                typesCorespondingPkmn.push({
                    TYPE: pkmnTypes[i],
                    LIST: []
                });

                let urlForList = pkmnTypes[i].TYPEURL;
                fetch(urlForList)
                    .then(res => res.json())
                    .then(data => {
                        //console.log(data.pokemon);
                        typesCorespondingPkmn[i].LIST = data.pokemon;
                    });
            }
            //console.log(typesCorespondingPkmn);
        });
    //console.log(moves);
    //console.log(pkmnTypes);
}

function fetchMoves() {
    fetch(baseUrl + 'move/')
        .then(res => res.json())
        .then(data => {
            let num = data.count;
            fetch(baseUrl + 'move?offset=0&limit=' + num)
                .then(res => res.json())
                .then(data => {
                    for (let i = 0; i < num; i++) {
                        fetch(data.results[i].url)
                            .then(res => res.json())
                            .then(data => {
                                moves.push({
                                    NAME: data.name,
                                    TYPE: data.type.name,
                                    POWER: data.power
                                });
                            });
                    }
                });
        });
}

function getCurrPkmnMoves(pkmn, data) {
    moveset.innerHTML = "";
    currentMoves = [];
    for (let i = 0; i < data.moves.length; i++) {
        let move = moves.find(name => name.NAME === data.moves[i].move.name);
        moveset.innerHTML +=
            `
                <div class="row">
                    <div class="col-sm-5 offset-1">${move.NAME}</div>
                    <div class="col-sm-2">${move.TYPE}</div>
                    <div class="col-sm-1" style="text-align: right;">${move.POWER != null ? move.POWER : ""}</div>
                </div>
            `
        currentMoves.push({
            NAME: move.NAME,
            TYPE: move.TYPE,
            POWER: move.POWER
        });
    }
    return currentMoves;
}

