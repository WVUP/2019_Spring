function filterPkmn() {
    const regex = new RegExp(".*" + pkmnName.value + ".*");
    let count = pkmnList.filter(pkmn => pkmn.NAME.match(regex)).length;
    pkmnListDiv.innerHTML = "";
    for (let i = 0; i < count; i++) {
        pkmnListDiv.innerHTML += `<p id="${pkmnList.filter(pkmn => pkmn.NAME.match(regex))[i].NAME}">${
            pkmnList.filter(pkmn => pkmn.NAME.match(regex))[i].NAME}</p>`;
    }
}

function filterByType() {
    let alreadyAdded = [];

    for(let i = 0; i < typesCorespondingPkmn.length; i++)
    {
        if(typesCorespondingPkmn[i].TYPE.TYPE == selectedType1.toLowerCase() ||
        typesCorespondingPkmn[i].TYPE.TYPE == (selectedType2 != null? selectedType2.toLowerCase(): "")){
            pkmnListForTypeFilter.push(typesCorespondingPkmn[i]);
        }
    }

    if(selectedType2 == null){
        //console.log(pkmnListForTypeFilter);
        pkmnListDiv.innerHTML = "";
        for(let i = 0; i < pkmnListForTypeFilter[0].LIST.length; i++){
            if(alreadyAdded.indexOf(makingThisEasier(i)) < 0){
                pkmnListDiv.innerHTML +=
                `
                <p id="${makingThisEasier(i) != ""? makingThisEasier(i): Math.random()}">
                ${makingThisEasier(i) != ""? makingThisEasier(i): "Some pkmn from Alolan region are not included."}
                </p>
                `
                alreadyAdded.push(makingThisEasier(i));
            }
        }
        alreadyAdded = [];
    }
}

function makingThisEasier(i){
    let subString = pkmnListForTypeFilter[0].LIST[i].pokemon.name.substring(0, pkmnListForTypeFilter[0].LIST[i].pokemon.name.indexOf("-"));
    //console.log("Substring = " + subString);
    try{
        pkmn = pkmnList.filter(pkmn => pkmn.NAME == pkmnListForTypeFilter[0].LIST[i].pokemon.name)[0].NAME;
        //console.log(pkmn);
    }catch(err){
        //console.log("Error from filter on " + pkmnListForTypeFilter[0].LIST[i].pokemon.name);
        try{
            pkmn = pkmnList.filter(pkmn => pkmn.NAME == subString)[0].NAME;
        }catch(err){
            //console.log("Error from filter on subString");
            pkmn = "";
        }
    }
    //console.log(pkmn);
    return pkmn
}

function removeIds() {
    for (let i = 0; i < types.length; i++) {
        if (types[i] != selectedType1 && types[i] != selectedType2) {
            document.getElementById(types[i]).removeAttribute('id');
        }
    }
}

function replaceIds() {
    let elements = document.getElementsByClassName("type");
    for (let i = 0; i < types.length; i++) {
        elements[i].setAttribute('id', types[i]);
    }
}

